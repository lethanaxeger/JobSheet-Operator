package pertemuan1;

public class LuasLingkaran {
    
    public static void main(String[] args) {
        
        // Deklrasi
        Double pi, r , luas;
        
        // Deskripsi
        r = 112.0;
        pi = 3.14;
        
        // Proses
        luas = pi * r * r;
        
        // Output
        System.out.println("r = 112 ");
        System.out.println("pi = 3.14 ");
        System.out.println("Luas Lingkaran = ");
        System.out.println(luas);
    }
}
